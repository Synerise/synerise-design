/**
 * Generates Icon.figma.tsx — Code Connect mappings for all icons in the
 * Synerise Design System Figma file.
 *
 * Usage:
 *   FIGMA_TOKEN=<token> node build/figma-code-connect.js
 *
 * Requires the FIGMA_TOKEN environment variable to be set.
 */

const fs = require('fs');
const path = require('path');

const FILE_KEY = 'fsSZONXpVvtrDsCgtu01Jb';
const COMPONENTS_URL = `https://api.figma.com/v1/files/${FILE_KEY}/components`;
const BASE_URL = `https://www.figma.com/design/${FILE_KEY}/Synerise-Design-System?node-id=`;

const ICON_PATTERN = /^\d{2}\s+(S|M|L|XL)\s*\//;

function figmaNameToIconName(name) {
  const last = name.split('/').pop().trim();
  const normalized = last.replace(/[_&]+/g, '-');
  const stripped = normalized.replace(/^(\d+[\s-]+)+/, '').replace(/^-+/, '');
  return stripped
    .split(/[^a-zA-Z0-9]+/)
    .filter(Boolean)
    .map((w) => w.charAt(0).toUpperCase() + w.slice(1).toLowerCase())
    .join('');
}

async function main() {
  const token = process.env.FIGMA_TOKEN;
  if (!token) {
    console.error('Error: FIGMA_TOKEN environment variable is required.');
    console.error('Usage: FIGMA_TOKEN=<token> node build/figma-code-connect.js');
    process.exit(1);
  }

  console.log('Fetching icon components from Figma...');
  const res = await fetch(COMPONENTS_URL, {
    headers: { 'X-Figma-Token': token },
  });

  if (!res.ok) {
    console.error(`Figma API error: ${res.status} ${res.statusText}`);
    process.exit(1);
  }

  const data = await res.json();
  const components = data.meta?.components || [];

  const seen = new Set();
  const icons = [];

  for (const c of components) {
    if (!ICON_PATTERN.test(c.name)) continue;

    const iconName = figmaNameToIconName(c.name);
    if (seen.has(iconName)) continue;
    seen.add(iconName);

    const nodeId = c.node_id.replace(':', '-');
    icons.push({ iconName, nodeId });
  }

  console.log(`Found ${icons.length} unique icons.`);

  const lines = [
    '// @ts-nocheck',
    '// Auto-generated by build/figma-code-connect.js — do not edit manually.',
    "import figma from '@figma/code-connect';",
    '',
    "import Icon from './Icon';",
    '',
  ];

  for (const { iconName, nodeId } of icons) {
    lines.push(
      `figma.connect(Icon, '${BASE_URL}${nodeId}&m=dev', {`,
      `  example: () => <Icon component={<${iconName} />} />,`,
      '});',
      '',
    );
  }

  const outPath = path.resolve(__dirname, '../src/Icon.figma.tsx');
  fs.writeFileSync(outPath, lines.join('\n'));
  console.log(`Written ${icons.length} icon connects to ${outPath}`);
}

main();
